
// finding the area of a cirlce by passing radius as arguemnt to the function
function AreaOfCircle(radius) {
	
	var piValue = 3.14159;

	var area = piValue * radius * radius;

	// printing on the browser console
	console.log("Area of circle with radius " +radius+ " is: " +area);
}


// Calling the function
AreaOfCircle(5);

// Calling the function
AreaOfCircle(8);